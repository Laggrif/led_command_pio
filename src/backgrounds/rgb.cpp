void drawHome(int r, int g, int b, int w, int a, int focusObject, bool focus){
    Heltec.display->clear();

    Heltec.display->drawXbm(0, 0, SLIDERS_WIDTH, SLIDERS_HEIGHT, SLIDERS);

    Heltec.display->drawXbm(117, focusObject * 6 + 1, ARROW_WIDTH, ARROW_HEIGHT, ARROW);

    drawProgress(r, 0);
    drawProgress(g, 1);
    drawProgress(b, 2);
    drawProgress(w, 3);
    drawProgress(a, 4);

    Heltec.display->fillRect(12, 0, 100, 1);
    
    Heltec.display->display();
}


void drawProgress(int progress, int focus){
    int top = 2 + 6 * focus;

    switch (progress)
    {
    case 0:
        return;
    case 100:
        Heltec.display->fillRect(12, top, progress + 2, 3);
        return;
    default:
        break;
    }

    Heltec.display->fillRect(12, top, progress + 1, 3);
    Heltec.display->setPixel(progress + 13, top + 1);
}